class Solution {
    public Node addOne(Node head) {
        // Reverse the linked list
        Node prev = null;
        Node curr = head;
        while (curr != null) {
            Node next = curr.next;
            curr.next = prev;
            prev = curr;
            curr = next;
        }
        head = prev; // Update the head to the reversed list

        // Add one to the reversed list
        Node temp = head;
        int carry = 1;
        while (temp != null) {
            temp.data += carry;
            if (temp.data >= 10) {
                temp.data -= 10;
                carry = 1;
            } else {
                carry = 0;
            }
            if (carry != 0 && temp.next == null) {
                temp.next = new Node(1);
                carry = 0;
            }
            temp = temp.next;
        }

        // Reverse the list again to restore the original order
        prev = null;
        curr = head;
        while (curr != null) {
            Node next = curr.next;
            curr.next = prev;
            prev = curr;
            curr = next;
        }
        head = prev; // Update the head to the final reversed list

        return head;
    }
}
